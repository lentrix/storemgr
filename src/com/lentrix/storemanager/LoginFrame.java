    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        String username = userNameField.getText();
        String password = String.valueOf(passwordField.getPassword());
        
        if(username.isBlank() || password.isBlank()) {
            Helper.error("You have to provide both user name and password.", this);
        }else {
            try {
                UserModel user = UserController.login(username, password);
                if(user!=null) {
                    Helper.currentUser = user;
                    this.dispose();
                    if(user.getRole().equals("cashier")) {
                        new POSTerminal().setVisible(true);
                    }else {
                        new ManagementFrame().setVisible(true);
                    }
                }else {
                    Helper.error("Invalid credentials", this);
                }
            }catch(SQLException ex) {
                Helper.error(ex.getMessage(), this);
            }
        }
    }//GEN-LAST:event_loginButtonActionPerformed

    

