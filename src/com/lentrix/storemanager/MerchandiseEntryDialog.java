    private boolean validateFields() {
        if(barCodeField.getText().isBlank()){
            Helper.error("Barcode cannot be blank", this);
            return false;
        }
        
        if(itemNameField.getText().isBlank()) {
            Helper.error("Item name cannot be blank", this);
            return false;
        }
        
        if(itemDescriptionField.getText().isBlank()) {
            Helper.error("Item description cannot be blank", this);
            return false;
        }
        
        if(volumeField.getText().isBlank()) {
            Helper.error("Item volume cannot be blank", this);
            return false;
        }
        
        if(wholesaleUnitField.getText().isBlank()) {
            Helper.error("Item wholesale unit cannot be blank", this);
            return false;
        }
        
        if(wholesaleQtyField.getText().isBlank()) {
            Helper.error("Item Wholesale Qty cannot be blank", this);
            return false;
        }
        
        if(wholesalePriceField.getText().isBlank()) {
            Helper.error("Item Wholesale price cannot be blank", this);
            return false;
        }
        
        if(retailUnitField.getText().isBlank()) {
            Helper.error("Item Retail unit cannot be blank", this);
            return false;
        }
        
        if(retailPriceField.getText().isBlank()) {
            Helper.error("Item Retail price cannot be blank", this);
            return false;
        }
        
        return true;
    }

