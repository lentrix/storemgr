    private void saveChangesButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveChangesButtonActionPerformed
        try {
            if(item==null) {

                item = new ItemModel(
                        -1, barCodeField.getText(),
                        itemNameField.getText(),
                        itemDescriptionField.getText(),
                        volumeField.getText(),
                        wholesaleUnitField.getText(),
                        Integer.parseInt(wholesaleQtyField.getText()),
                        Float.parseFloat(wholesalePriceField.getText()),
                        retailUnitField.getText(),
                        Float.parseFloat(retailPriceField.getText()),
                        Integer.parseInt(quantityField.getText())
                );
                
                ItemController.insert(item);

            }else {
                item.setBarCode(barCodeField.getText());
                item.setItemName(itemNameField.getText());
                item.setItemDescription(itemDescriptionField.getText());
                item.setVolume(volumeField.getText());
                item.setWsUnit(wholesaleUnitField.getText());
                item.setWsQty(Integer.parseInt(wholesaleQtyField.getText()));
                item.setWsPrice(Float.parseFloat(wholesalePriceField.getText()));
                item.setRtUnit(retailUnitField.getText());
                item.setRtPrice(Float.parseFloat(retailPriceField.getText()));
                item.setQty(Integer.parseInt(quantityField.getText()));
                ItemController.update(item);
            }
            
            this.dispose();
        }catch(NumberFormatException ex) {
            Helper.error("There are some invalid numeric entries.", this);
        }catch(SQLException ex) {
            Helper.error(ex.getMessage(), this);
        }
    }//GEN-LAST:event_saveChangesButtonActionPerformed

