    private void refreshTable(String filter) {
        try {
            
            customers = CustomerController.get(filter);
            
            String[] headers = {"Name", "Address", "Phone Number", "Credit Limit"};
            DefaultTableModel model = new DefaultTableModel(headers, 0){
                @Override
                public boolean isCellEditable(int row, int column) {
                    return false;
                }
                
            };
            
            for(CustomerModel customer: customers) {
                model.addRow(new Object[] {
                    customer.getName(),
                    customer.getAddress(),
                    customer.getPhone(),
                    String.format("%,.2f", customer.getCreditLimit())
                });
            }
            
            jTable1.setModel(model);
            jTable1.getColumnModel().getColumn(1).setMinWidth(250);
            jTable1.getColumnModel().getColumn(0).setMinWidth(180);
            DefaultTableCellRenderer rightRenderer = new DefaultTableCellRenderer();
            rightRenderer.setHorizontalAlignment(JLabel.RIGHT);
            jTable1.getColumnModel().getColumn(3).setCellRenderer(rightRenderer);
            
        }catch(SQLException ex) {
            Helper.error(ex.getMessage(), this);
        }
    }

