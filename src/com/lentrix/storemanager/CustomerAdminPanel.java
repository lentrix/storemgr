    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        int row = jTable1.getSelectedRow();
        CustomerModel customer = customers.get(row);
        
        int res = JOptionPane.showConfirmDialog(this, "Do you want to delete customer " + customer.getName() + "?", "Delete?", JOptionPane.YES_NO_OPTION);
        
        if(res==JOptionPane.YES_OPTION) {
            try {
                CustomerController.delete(customer);
                refreshTable(filterField.getText());
            }catch(SQLException ex) {
                Helper.error(ex.getMessage(), this);
            }
        }
    }//GEN-LAST:event_deleteButtonActionPerformed


